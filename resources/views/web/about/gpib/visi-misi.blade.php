<h4 class="text-dark font-weight-bold mb-2">Visi</h4>
<p class="mb-4">GPIB menjadi gereja yang mewujudkan damai sejahtera bagi seluruh ciptaanNya.</p>

<h4 class="text-dark font-weight-bold">Misi</h4>
<ol class="mb-4 text-justify">
    <li class="mb-2"><p>Menjadi Gereja yang terus menerus diperbaharui dengan bertolak dari Firman Allah, yang terwujud dalam perilaku kehidupan warga gereja, baik dalam persekutuan, maupun dalam hidup bermasyarakat.</p></li>
    <li class="mb-2"><p>Menjadi gereja yang hadir sebagai contoh kehidupan, yang terwujud melalui inisiatif dan partisipasi dalam kesetiakawanan sosial serta kerukunan dalam masyarakat, dengan berbasis pada perilaku kehidupan keluarga yang kuat dan sejahtera.</p></li>
    <li class="mb-2"><p>Menjadi Gereja yang membangun keutuhan ciptaan yang terwujud melalui perhatian terhadap lingkungan hidup, semangat keesaan dan semangat persatuan dan kesatuan warga Gereja sebagai warga masyarakat.</p></li>
</ol>

